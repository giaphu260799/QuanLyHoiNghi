    private void xacnhanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_xacnhanActionPerformed
        if (masuatruycap.getText().trim().isEmpty())
            JOptionPane.showMessageDialog(null,"Không được để trống mã tài khoản!!.","Thông báo",JOptionPane.ERROR_MESSAGE);
        else
        {
             Object[] options = {"Đồng ý","Hủy"};
            int YesOrNo = JOptionPane.showOptionDialog(null,
                "Xác nhận đổi quyền truy cập ?",
                "Thông báo",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.QUESTION_MESSAGE,
                null,
                options,
                options[1]);
            if(YesOrNo == 0)
            {
                Session session = HibernateUtil.getSessionFactory().openSession();
                session.beginTransaction();
                String hql;
                if(suatruycap.getSelectedItem().toString().compareTo("Cấm") == 0)
                    hql = ("update tai_khoan set truy_cap = false where id = :id");
                else hql = ("update tai_khoan set truy_cap = true where id  = :id");
                Query q =session.createSQLQuery(hql);
                q.setParameter("id",masuatruycap.getText());
                int n = q.executeUpdate();
                session.getTransaction().commit();
                if(n>0)
                {
                    JOptionPane.showMessageDialog(null,"Đổi quyền truy cập thành công!!.","Thông báo",JOptionPane.INFORMATION_MESSAGE);
                    displayTable();
                }
                    else JOptionPane.showMessageDialog(null,"Mã tài khoản không tồn tại!!.","Thông báo",JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_xacnhanActionPerformed

