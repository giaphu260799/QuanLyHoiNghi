    public void displayTable()
    {
        Session session = HibernateUtil.getSessionFactory().openSession();
        session.beginTransaction();
        Query q = session.createQuery("select t from TaiKhoan t where t.username != 'admin'");
        List resultList = q.list();
        session.getTransaction().commit();
        Vector<String> tableHeaders = new Vector<String>();
        Vector tableData = new Vector();
        tableHeaders.add("Mã tài khoản"); 
        tableHeaders.add("Username"); 
        tableHeaders.add("Password");
        tableHeaders.add("Tên"); 
        tableHeaders.add("Email");
        tableHeaders.add("Truy cập"); 
        for(Object o : resultList) {
            TaiKhoan t =(TaiKhoan)o;
            Vector<Object> oneRow = new Vector<Object>();
            oneRow.add(t.getId());
            oneRow.add(t.getUsername());
            oneRow.add(t.getPass());
            oneRow.add(t.getTen());
            oneRow.add(t.getEmail());
            if(t.isTruyCap())
                oneRow.add("Được phép");
            else oneRow.add("Cấm");
            tableData.add(oneRow);
        }
        resultTableu.setModel(new DefaultTableModel(tableData, tableHeaders));
        resultTableu.setDefaultEditor(Object.class, null);
        resultTableu.getTableHeader().setFont(new java.awt.Font("Tahoma", 0, 18));
        resultTableu.setAutoCreateRowSorter(true);
    }
    
